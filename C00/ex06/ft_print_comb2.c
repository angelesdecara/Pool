#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	a;

	i = -1;
	j = 0;
	while (++i < 99)
	{
		while (++j <= 99)
		{
			a = i / 10 + '0';
			write(1, &a, 1);
			a = i % 10 + '0';
			write(1, &a, 1);
			write(1, " ", 1);
			a = j / 10 + '0';
			write(1, &a, 1);
			a = j % 10 + '0';
			write(1, &a, 1);
			if (!(i == 98 && j == 99))
				write(1, ", ", 2);
		}
		j = i + 1;
	}
}
