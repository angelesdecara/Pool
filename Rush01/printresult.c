#include "header.h"

void	imprimesols(int	*sols)
{
	int		i;
	char	a;

	i = 0;
	while (i < 16)
	{
		a = sols[i] + '0';
		write(1, &a, 1);
		write(1, " ",1);
		if ((i + 1) % 4 == 0 && i != 16)
			write(1, "\n",1);
		i++;
	}
}
