int	ft_atoi(char *str)
{
	int	i;
	int	len;
	int	res;

	i = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str == '+' || *str == '-')
	{
		i *= (1 - *str % 43);
		str++;
	}
	len = i;
	i = 0;
	res = 0;
	while (*str >= '0' && *str <= '9')
	{
		res = (*str - 48) + res * 10;
		str++;
		i++;
	}
	return (len * res);
}
