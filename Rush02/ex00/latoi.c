#include "header.h"

unsigned long int	latoi(char *str)
{
	unsigned long int	res;

	res = 0;
	while (*str != '\0' && (*str <= '9' && *str >= '0'))
	{
		res = (*str - 48) + 10 * res;
		str++;
	}
	return (res);
}
